// =============================================================================
// ec2-lifecycle.tpl.ts — EC2 instance lifecycle decision helpers
//
// Pure functions (no AWS calls) that determine whether an EC2 instance
// should be replaced, updated, or adopted from legacy state.
//
// Key design decision: Alchemy's Cloud Control EC2 provider compares
// desired state against the live AWS model, which includes synthetic
// fields like SecurityGroups (by name). That causes false replacements.
// Instead, this module stores a normalized snapshot of the desired props
// in Alchemy state and compares snapshot-to-snapshot, ignoring whatever
// extra fields AWS returns.
//
// Generated by stackpanel — static library code.
// =============================================================================

// ---- Types ------------------------------------------------------------------

export interface Ec2InstanceProps {
  name: string;
  ami: string;
  instanceType: string;
  subnetId: string;
  securityGroupIds: string[];
  keyName?: string | null;
  iamInstanceProfile?: string | null;
  userData?: string | null;
  rootVolumeSize?: number | null;
  associatePublicIp?: boolean;
  tags?: Record<string, string>;
  destroyOnDelete?: boolean;
}

/** Deterministic representation of EC2 props, used for diffing across deploys. */
export interface NormalizedEc2InstanceProps {
  ami: string;
  instanceType: string;
  subnetId: string;
  securityGroupIds: string[];
  keyName: string | null;
  iamInstanceProfile: string | null;
  userData: string | null;
  rootVolumeSize: number | null;
  associatePublicIp: boolean;
  tags: Record<string, string>;
}

export interface Ec2Instance {
  instanceId: string;
  publicIp?: string | null;
  privateIp?: string | null;
  publicDns?: string | null;
  state?: string | null;
  name: string;
  /** Stored snapshot of the props that created/last updated this instance. */
  propsSnapshot: NormalizedEc2InstanceProps;
}

// ---- Normalization ----------------------------------------------------------

function sortRecord(value: Record<string, string> | undefined): Record<string, string> {
  return Object.fromEntries(
    Object.entries(value ?? {}).sort(([left], [right]) => left.localeCompare(right)),
  );
}

/**
 * Convert raw Ec2InstanceProps into a deterministic snapshot.
 * Sorts arrays and records so ordering differences don't trigger replacements.
 */
export function normalizeEc2InstanceProps(
  props: Ec2InstanceProps,
): NormalizedEc2InstanceProps {
  return {
    ami: props.ami,
    instanceType: props.instanceType,
    subnetId: props.subnetId,
    securityGroupIds: [...props.securityGroupIds].sort(),
    keyName: props.keyName ?? null,
    iamInstanceProfile: props.iamInstanceProfile ?? null,
    userData: props.userData ?? null,
    rootVolumeSize: props.rootVolumeSize ?? null,
    associatePublicIp: props.associatePublicIp ?? true,
    tags: sortRecord(props.tags),
  };
}

// ---- Replacement detection --------------------------------------------------

/**
 * Returns true if any immutable property changed between deploys.
 * Tag changes are NOT immutable — they get synced in-place.
 */
export function requiresEc2InstanceReplacement(
  previous: NormalizedEc2InstanceProps,
  next: NormalizedEc2InstanceProps,
): boolean {
  return (
    previous.ami !== next.ami ||
    previous.instanceType !== next.instanceType ||
    previous.subnetId !== next.subnetId ||
    JSON.stringify(previous.securityGroupIds) !== JSON.stringify(next.securityGroupIds) ||
    previous.keyName !== next.keyName ||
    previous.iamInstanceProfile !== next.iamInstanceProfile ||
    previous.userData !== next.userData ||
    previous.rootVolumeSize !== next.rootVolumeSize ||
    previous.associatePublicIp !== next.associatePublicIp
  );
}

// ---- Legacy state migration -------------------------------------------------
//
// When migrating from AWS Cloud Control's EC2 provider (AWS::EC2::Instance)
// to this custom resource, the Alchemy state file contains CloudFormation-
// shaped output (PascalCase fields, desiredState blob). These functions
// coerce that legacy shape into our Ec2Instance type so the resource can
// adopt the existing instance without forcing a replacement.

/** Convert AWS CloudFormation Tag array [{Key, Value}] to a plain record. */
function tagsToRecord(value: unknown): Record<string, string> {
  if (!Array.isArray(value)) return {};

  return Object.fromEntries(
    value
      .flatMap((tag) => {
        if (!tag || typeof tag !== "object") return [];
        const record = tag as { Key?: unknown; Value?: unknown };
        if (typeof record.Key !== "string" || typeof record.Value !== "string") return [];
        return [[record.Key, record.Value] as const];
      })
      .sort(([left], [right]) => left.localeCompare(right)),
  );
}

/**
 * Best-effort coercion of legacy Cloud Control output into Ec2Instance shape.
 * Falls back gracefully — any field that can't be read is set to null/undefined.
 */
export function coerceEc2InstanceState(output: unknown): Partial<Ec2Instance> {
  if (!output || typeof output !== "object") return {};

  const record = output as Record<string, any>;
  const desiredState =
    record.desiredState && typeof record.desiredState === "object"
      ? (record.desiredState as Record<string, any>)
      : {};

  // Cloud Control nests networking under NetworkInterfaces[0]
  const networkInterface =
    desiredState.NetworkInterfaces?.[0] ?? record.NetworkInterfaces?.[0] ?? {};
  const securityGroupIds =
    desiredState.SecurityGroupIds ?? networkInterface.GroupSet ?? record.SecurityGroupIds ?? [];
  const subnetId =
    desiredState.SubnetId ?? networkInterface.SubnetId ?? record.SubnetId;
  const ami = desiredState.ImageId ?? record.ImageId;
  const instanceType = desiredState.InstanceType ?? record.InstanceType;

  let propsSnapshot: NormalizedEc2InstanceProps | undefined;
  if (
    typeof ami === "string" &&
    typeof instanceType === "string" &&
    typeof subnetId === "string" &&
    Array.isArray(securityGroupIds)
  ) {
    const desiredMappings = desiredState.BlockDeviceMappings ?? record.BlockDeviceMappings ?? [];
    const rootVolumeSize = desiredMappings?.[0]?.Ebs?.VolumeSize;

    propsSnapshot = normalizeEc2InstanceProps({
      name: String(record.name ?? "legacy"),
      ami,
      instanceType,
      subnetId,
      securityGroupIds: securityGroupIds.filter(
        (value: unknown): value is string => typeof value === "string",
      ),
      keyName:
        typeof (desiredState.KeyName ?? record.KeyName) === "string"
          ? (desiredState.KeyName ?? record.KeyName) : null,
      iamInstanceProfile:
        typeof (desiredState.IamInstanceProfile ?? record.IamInstanceProfile) === "string"
          ? (desiredState.IamInstanceProfile ?? record.IamInstanceProfile) : null,
      userData:
        typeof (desiredState.UserData ?? record.UserData) === "string"
          ? (desiredState.UserData ?? record.UserData) : null,
      rootVolumeSize: typeof rootVolumeSize === "number" ? rootVolumeSize : null,
      associatePublicIp:
        typeof networkInterface.AssociatePublicIpAddress === "boolean"
          ? networkInterface.AssociatePublicIpAddress : true,
      tags: tagsToRecord(desiredState.Tags ?? record.Tags),
    });
  }

  return {
    instanceId:
      (typeof record.instanceId === "string" ? record.instanceId : null) ??
      (typeof record.InstanceId === "string" ? record.InstanceId : null) ??
      (typeof record.id === "string" ? record.id : null) ??
      undefined,
    publicIp:
      (typeof record.publicIp === "string" ? record.publicIp : null) ??
      (typeof record.PublicIp === "string" ? record.PublicIp : null) ?? null,
    privateIp:
      (typeof record.privateIp === "string" ? record.privateIp : null) ??
      (typeof record.PrivateIp === "string" ? record.PrivateIp : null) ??
      (typeof record.PrivateIpAddress === "string" ? record.PrivateIpAddress : null) ?? null,
    publicDns:
      (typeof record.publicDns === "string" ? record.publicDns : null) ??
      (typeof record.PublicDnsName === "string" ? record.PublicDnsName : null) ?? null,
    state:
      (typeof record.state === "string" ? record.state : null) ??
      (typeof record.State?.Name === "string" ? record.State.Name : null) ?? null,
    name: typeof record.name === "string" ? record.name : "legacy",
    propsSnapshot,
  };
}

// ---- Phase guards -----------------------------------------------------------

/**
 * During Alchemy's replace flow, the resource is called in a create-like
 * phase but still receives the OLD (terminated) output. We must not reuse
 * a terminated instance's ID — only update phases should inherit state.
 */
export function shouldReuseEc2InstanceState(
  phase: string,
  output: Partial<Ec2Instance> | null | undefined,
): boolean {
  return phase === "update" && Boolean(output?.instanceId);
}

/**
 * An instance is "ready" when it's running and, for public instances,
 * has been assigned at least an IP or DNS name.
 */
export function isEc2InstanceReady(
  instance: {
    State?: { Name?: string | null } | null;
    PublicIpAddress?: string | null;
    PublicDnsName?: string | null;
  } | null | undefined,
  requirePublicEndpoint: boolean,
): boolean {
  if (instance?.State?.Name !== "running") return false;
  if (!requirePublicEndpoint) return true;
  return Boolean(instance.PublicIpAddress || instance.PublicDnsName);
}
