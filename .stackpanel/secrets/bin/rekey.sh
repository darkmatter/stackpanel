#!/usr/bin/env bash
# ==============================================================================
# rekey.sh - Re-encrypt .enc.age files to current recipients
#
# When a new team member's .age.pub file is added, this script re-encrypts
# all .enc.age files so the new recipient can decrypt them.
#
# This script requires only `sops` in PATH (no stackpanel CLI needed).
# It reads recipient public keys from .age.pub files (flat + group subdirs)
# and re-encrypts each .enc.age file to all recipients.
#
# Usage:
#   ./rekey.sh                   # re-encrypt all .enc.age files
#   ./rekey.sh <group>           # re-encrypt a specific group's .enc.age
#
# Dependencies: sops
# ==============================================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SECRETS_DIR="$(dirname "$SCRIPT_DIR")"
RECIPIENTS_DIR="$SECRETS_DIR/recipients"

usage() {
	echo "Usage: $(basename "$0") [group]"
	echo ""
	echo "Re-encrypt .enc.age files to all current recipients."
	echo "This is needed when new team members are added."
	echo ""
	echo "Arguments:"
	echo "  [group]    Optional: re-encrypt only this group (default: all)"
	echo ""
	echo "Recipients are read from:"
	echo "  $RECIPIENTS_DIR/*.age.pub          (standalone recipients)"
	echo "  $RECIPIENTS_DIR/<group>/*.age.pub  (group recipients)"
	exit 1
}

if [[ "${1:-}" == "-h" || "${1:-}" == "--help" ]]; then
	usage
fi

# Helper: collect AGE public keys from a directory
# Supports *.age.pub (native AGE) and *.ed25519.pub (SSH, converted via ssh-to-age)
collect_pubs_from_dir() {
	local dir="$1"
	# Native AGE keys
	for pub_file in "$dir"/*.age.pub; do
		[[ -f "$pub_file" ]] || continue
		PUB_KEY=$(tr -d '[:space:]' <"$pub_file")
		[[ -n "$PUB_KEY" ]] && ALL_PUBS="$ALL_PUBS $PUB_KEY"
	done
	# SSH ED25519 keys (converted to AGE via ssh-to-age)
	for pub_file in "$dir"/*.ed25519.pub; do
		[[ -f "$pub_file" ]] || continue
		if command -v ssh-to-age &>/dev/null; then
			AGE_KEY=$(ssh-to-age <"$pub_file" 2>/dev/null) || true
			if [[ -n "$AGE_KEY" && "$AGE_KEY" == age1* ]]; then
				ALL_PUBS="$ALL_PUBS $AGE_KEY"
			else
				echo "Warning: Could not convert $(basename "$pub_file") to AGE key" >&2
			fi
		else
			echo "Warning: ssh-to-age not found, skipping $(basename "$pub_file")" >&2
		fi
	done
	# Reject RSA keys
	for pub_file in "$dir"/*.rsa.pub; do
		[[ -f "$pub_file" ]] || continue
		echo "Error: RSA keys are not supported by age. Remove $(basename "$pub_file")" >&2
	done
}

# Collect all recipient public keys
ALL_PUBS=""
collect_pubs_from_dir "$RECIPIENTS_DIR"
for group_dir in "$RECIPIENTS_DIR"/*/; do
	[[ -d "$group_dir" ]] || continue
	[[ "$(basename "$group_dir")" == ".archive" ]] && continue
	collect_pubs_from_dir "$group_dir"
done
# Deduplicate
ALL_PUBS=$(echo "$ALL_PUBS" | tr ' ' '\n' | sort -u | tr '\n' ' ' | sed 's/^ //;s/ $//')

if [[ -z "$ALL_PUBS" ]]; then
	echo "Error: No recipient public keys found" >&2
	exit 1
fi

COUNT=$(echo "$ALL_PUBS" | wc -w | tr -d ' ')
echo "Found $COUNT unique recipient(s)"

# Regenerate recipients/.sops.yaml
AGE_LINES=""
for PUB_KEY in $ALL_PUBS; do
	AGE_LINES="$AGE_LINES
            - $PUB_KEY"
done

cat >"$RECIPIENTS_DIR/.sops.yaml" <<SOPSEOF
# Auto-generated by rekey.sh
# DO NOT EDIT — changes will be overwritten.

creation_rules:
  - path_regex: \.enc\.age\$
    key_groups:
      - age:$AGE_LINES
SOPSEOF
echo "Regenerated recipients/.sops.yaml"

# Re-encrypt .enc.age files
rekey_group() {
	local group="$1"
	local enc_file="$RECIPIENTS_DIR/$group.enc.age"
	local plain_file="$RECIPIENTS_DIR/$group.age"

	if [[ ! -f "$enc_file" ]]; then
		echo "Error: $enc_file not found" >&2
		return 1
	fi

	# We need the plaintext private key to re-encrypt.
	# Resolution order:
	#   1. Environment variable SECRET_<GROUP_UPPER> (CI workflow)
	#   2. Plaintext .age file (local, gitignored)
	#   3. sops --decrypt .enc.age (requires local key as recipient)
	local private_key=""
	local group_upper
	group_upper=$(echo "$group" | tr '[:lower:]' '[:upper:]' | tr '-' '_')
	local env_var="SECRET_${group_upper}"
	if [[ -n "${!env_var:-}" ]]; then
		private_key="${!env_var}"
	elif [[ -f "$plain_file" ]]; then
		private_key=$(cat "$plain_file")
	else
		# Try to decrypt the .enc.age to get the private key
		if private_key=$(sops --decrypt "$enc_file" 2>/dev/null); then
			:
		else
			echo "  $group: SKIPPED (cannot decrypt .enc.age - need plaintext .age file or SECRET_${group_upper} env var)" >&2
			return 1
		fi
	fi

	# Write to temp, encrypt, replace
	local tmpdir
	tmpdir=$(mktemp -d)
	trap "rm -rf '$tmpdir'" RETURN

	echo "$private_key" >"$tmpdir/plain.txt"
	if sops --config "$RECIPIENTS_DIR/.sops.yaml" \
		--filename-override "$enc_file" \
		--encrypt --input-type binary --output-type binary \
		"$tmpdir/plain.txt" >"$tmpdir/encrypted.bin" 2>/dev/null; then
		mv "$tmpdir/encrypted.bin" "$enc_file"
		echo "  $group: re-encrypted"
	else
		echo "  $group: FAILED to re-encrypt" >&2
		return 1
	fi
}

FILTER="${1:-}"
REKEY_COUNT=0
ERRORS=0

for enc_file in "$RECIPIENTS_DIR"/*.enc.age; do
	[[ -f "$enc_file" ]] || continue
	group=$(basename "$enc_file" .enc.age)

	if [[ -n "$FILTER" && "$group" != "$FILTER" ]]; then
		continue
	fi

	rekey_group "$group" && ((REKEY_COUNT++)) || ((ERRORS++))
done

echo ""
echo "Re-encrypted $REKEY_COUNT group key(s)"
if [[ $ERRORS -gt 0 ]]; then
	echo "Warning: $ERRORS group(s) had errors" >&2
	exit 1
fi
