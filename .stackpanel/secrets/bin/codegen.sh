#!/usr/bin/env bash
# ==============================================================================
# codegen.sh - Generate typed environment modules from secrets config
#
# This script generates type-safe environment access code from the secrets
# directory structure, without requiring the stackpanel CLI or Nix.
#
# It reads:
#   - apps.json     (per-app codegen settings: language, output path)
#   - groups.json   (recipient group -> keygroup mapping)
#   - vars/*.sops.yaml (secret/config key names)
#
# And generates typed modules that provide validated env var access.
#
# Usage:
#   ./codegen.sh                 # generate for all apps
#   ./codegen.sh --app <name>    # generate for a specific app
#
# Dependencies: sops, jq
# ==============================================================================
set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
SECRETS_DIR="$(dirname "$SCRIPT_DIR")"
APPS_JSON="$SECRETS_DIR/apps.json"
GROUPS_JSON="$SECRETS_DIR/groups.json"
VARS_DIR="$SECRETS_DIR/vars"

usage() {
	echo "Usage: $(basename "$0") [--app <name>]"
	echo ""
	echo "Generate typed environment modules from secrets config."
	echo ""
	echo "Options:"
	echo "  --app <name>   Generate for a specific app only"
	echo ""
	echo "Reads configuration from:"
	echo "  $APPS_JSON     Per-app codegen settings"
	echo "  $GROUPS_JSON   Recipient group -> keygroup mapping"
	echo "  $VARS_DIR/     SOPS-encrypted value files"
	exit 1
}

if [[ "${1:-}" == "-h" || "${1:-}" == "--help" ]]; then
	usage
fi

if [[ ! -f "$APPS_JSON" ]]; then
	echo "Error: $APPS_JSON not found" >&2
	echo "Create it with per-app codegen settings, e.g.:" >&2
	echo '  { "web": { "language": "typescript", "path": "packages/gen/env/src/generated" } }' >&2
	exit 1
fi

APP_FILTER=""
while [[ $# -gt 0 ]]; do
	case "$1" in
	--app)
		APP_FILTER="$2"
		shift 2
		;;
	*)
		echo "Unknown option: $1" >&2
		usage
		;;
	esac
done

# Collect available keygroups and their keys
declare -A GROUP_KEYS
for vars_file in "$VARS_DIR"/*.sops.yaml; do
	[[ -f "$vars_file" ]] || continue
	group=$(basename "$vars_file" .sops.yaml)

	# Try to extract key names (without decrypting values)
	# sops filestatus shows which keys exist
	keys=$(sops decrypt "$vars_file" 2>/dev/null | grep -oE '^[a-z0-9][a-z0-9_-]*:' | sed 's/:$//' | sort) || keys=""
	GROUP_KEYS["$group"]="$keys"
done

# Process each app
for app in $(jq -r 'keys[]' "$APPS_JSON"); do
	if [[ -n "$APP_FILTER" && "$app" != "$APP_FILTER" ]]; then
		continue
	fi

	language=$(jq -r --arg a "$app" '.[$a].language // "typescript"' "$APPS_JSON")
	output_path=$(jq -r --arg a "$app" '.[$a].path // ""' "$APPS_JSON")

	if [[ -z "$output_path" ]]; then
		echo "Warning: No output path configured for app '$app' in apps.json" >&2
		continue
	fi

	echo "Generating $language env module for $app -> $output_path"

	# Determine which keygroups this app has access to
	app_groups=""
	if [[ -f "$GROUPS_JSON" ]]; then
		# Find recipient groups that include this app
		for rg in $(jq -r 'keys[]' "$GROUPS_JSON"); do
			rg_apps=$(jq -r --arg rg "$rg" '.[$rg].apps // [] | .[]' "$GROUPS_JSON" 2>/dev/null)
			rg_groups=$(jq -r --arg rg "$rg" '.[$rg].groups // [] | .[]' "$GROUPS_JSON")

			# If apps is empty, it applies to all apps
			if [[ -z "$rg_apps" ]] || echo "$rg_apps" | grep -qx "$app"; then
				app_groups="$app_groups $rg_groups"
			fi
		done
	fi

	# Fallback: use all available groups
	if [[ -z "$app_groups" ]]; then
		app_groups=$(echo "${!GROUP_KEYS[@]}" | tr ' ' '\n')
	fi

	# Deduplicate
	app_groups=$(echo "$app_groups" | tr ' ' '\n' | sort -u | tr '\n' ' ' | sed 's/^ //;s/ $//')

	if [[ "$language" == "typescript" ]]; then
		mkdir -p "$output_path/$app"

		for group in $app_groups; do
			keys="${GROUP_KEYS[$group]:-}"
			if [[ -z "$keys" ]]; then
				continue
			fi

			# Generate TypeScript module
			ts_file="$output_path/$app/$group.ts"
			{
				echo "// Auto-generated by codegen.sh — DO NOT EDIT"
				echo "import { z } from 'zod';"
				echo "import { parseEnv } from '../../znv';"
				echo ""
				echo "export const getEnv = () => parseEnv(process.env, {"
				while IFS= read -r key; do
					[[ -n "$key" ]] && echo "  $key: z.string(),"
				done <<<"$keys"
				echo "});"
			} >"$ts_file"
			echo "  Generated: $ts_file"
		done

		# Barrel export
		{
			echo "// Auto-generated by codegen.sh — DO NOT EDIT"
			for group in $app_groups; do
				[[ -n "${GROUP_KEYS[$group]:-}" ]] && echo "export * as $group from './$group';"
			done
		} >"$output_path/$app/index.ts"
	fi
done

echo ""
echo "Done!"
