func TestComputePayloadHash(t *testing.T) {
	t.Parallel()

	t.Run("deterministic for same input", func(t *testing.T) {
		t.Parallel()
		plaintext := []byte(`{"KEY":"value"}` + "\n")
		recipients := []string{"age1abc", "age1def"}
		h1 := computePayloadHash(plaintext, recipients)
		h2 := computePayloadHash(plaintext, recipients)
		if h1 != h2 {
			t.Fatalf("hash not deterministic: %q != %q", h1, h2)
		}
	})

	t.Run("recipients sorted before hashing", func(t *testing.T) {
		t.Parallel()
		plaintext := []byte(`{"KEY":"value"}` + "\n")
		h1 := computePayloadHash(plaintext, []string{"age1zzz", "age1aaa"})
		h2 := computePayloadHash(plaintext, []string{"age1aaa", "age1zzz"})
		if h1 != h2 {
			t.Fatalf("hash differs by recipient order: %q != %q", h1, h2)
		}
	})

	t.Run("different plaintext yields different hash", func(t *testing.T) {
		t.Parallel()
		r := []string{"age1abc"}
		h1 := computePayloadHash([]byte(`{"KEY":"v1"}`+"\n"), r)
		h2 := computePayloadHash([]byte(`{"KEY":"v2"}`+"\n"), r)
		if h1 == h2 {
			t.Fatal("expected different hashes for different plaintexts")
		}
	})

	t.Run("different recipients yield different hash", func(t *testing.T) {
		t.Parallel()
		p := []byte(`{"KEY":"value"}` + "\n")
		h1 := computePayloadHash(p, []string{"age1abc"})
		h2 := computePayloadHash(p, []string{"age1xyz"})
		if h1 == h2 {
			t.Fatal("expected different hashes for different recipients")
		}
	})

	t.Run("returns hex string", func(t *testing.T) {
		t.Parallel()
		h := computePayloadHash([]byte("test"), []string{"age1abc"})
		if len(h) != 64 {
			t.Fatalf("expected 64-char hex, got %d chars: %q", len(h), h)
		}
		for _, c := range h {
			if !strings.ContainsRune("0123456789abcdef", c) {
				t.Fatalf("non-hex char in hash: %q", h)
			}
		}
	})
}

func TestExtractPayloadHashFromModule(t *testing.T) {
	t.Parallel()

	t.Run("extracts hash from valid module", func(t *testing.T) {
		t.Parallel()
		content := "// Auto-generated by Stackpanel — do not edit manually.\n" +
			"// content-hash: abc123def456\n" +
			"const encryptedPayload = \"data\";\n\n" +
			"export default encryptedPayload;\n"
		path := filepath.Join(t.TempDir(), "module.ts")
		if err := os.WriteFile(path, []byte(content), 0644); err != nil {
			t.Fatal(err)
		}
		got, err := extractPayloadHashFromModule(path)
		if err != nil {
			t.Fatalf("unexpected error: %v", err)
		}
		if got != "abc123def456" {
			t.Fatalf("expected 'abc123def456', got %q", got)
		}
	})

	t.Run("returns empty string when no hash line", func(t *testing.T) {
		t.Parallel()
		content := "// Auto-generated by Stackpanel — do not edit manually.\n" +
			"const encryptedPayload = \"data\";\n\n" +
			"export default encryptedPayload;\n"
		path := filepath.Join(t.TempDir(), "old-module.ts")
		if err := os.WriteFile(path, []byte(content), 0644); err != nil {
			t.Fatal(err)
		}
		got, err := extractPayloadHashFromModule(path)
		if err != nil {
			t.Fatalf("unexpected error: %v", err)
		}
		if got != "" {
			t.Fatalf("expected empty string, got %q", got)
		}
	})

	t.Run("returns error for missing file", func(t *testing.T) {
		t.Parallel()
		path := filepath.Join(t.TempDir(), "missing.ts")
		_, err := extractPayloadHashFromModule(path)
		if err == nil {
			t.Fatal("expected error for missing file")
		}
	})
}

