// sopsAvailable returns true when sops is present in PATH.
func sopsAvailable() bool {
	_, err := exec.LookPath("sops")
	return err == nil
}

// requireSopsKey generates a temporary AGE key for integration tests.
// Returns the key path and the public key string.
func requireSopsKey(t *testing.T) (keyPath, pubKey string) {
	t.Helper()
	if _, err := exec.LookPath("age-keygen"); err != nil {
		t.Skip("age-keygen not in PATH")
	}

	dir := t.TempDir()
	keyPath = filepath.Join(dir, "key.txt")
	out, err := exec.Command("age-keygen", "-o", keyPath).CombinedOutput()
	if err != nil {
		t.Fatalf("age-keygen: %v: %s", err, out)
	}

	data, err := os.ReadFile(keyPath)
	if err != nil {
		t.Fatal(err)
	}
	for _, line := range bytes.Split(data, []byte("\n")) {
		if bytes.HasPrefix(line, []byte("# public key: ")) {
			pubKey = string(bytes.TrimPrefix(line, []byte("# public key: ")))
			return
		}
	}
	t.Fatal("could not extract public key from age-keygen output")
	return
}

func TestBuildSkipsReencryptionWhenUnchanged(t *testing.T) {
	// Cannot use t.Parallel() with t.Setenv.
	if !sopsAvailable() {
		t.Skip("sops not in PATH")
	}
	keyPath, pubKey := requireSopsKey(t)
	t.Setenv("SOPS_AGE_KEY_FILE", keyPath)

	projectRoot := t.TempDir()
	genDir := filepath.Join(projectRoot, ".stack", "gen", "codegen")
	if err := os.MkdirAll(genDir, 0755); err != nil {
		t.Fatal(err)
	}
	dataDir := filepath.Join(projectRoot, "packages", "gen", "env", "data", "myapp")
	if err := os.MkdirAll(dataDir, 0755); err != nil {
		t.Fatal(err)
	}

	outputPath := "packages/gen/env/data/myapp/dev.sops.json"
	manifest := envManifest{
		SchemaVersion: 1,
		DataRoot:      "packages/gen/env/data",
		Targets: []envTargetSpec{
			{
				App:         "myapp",
				Environment: "dev",
				OutputPath:  outputPath,
				Recipients:  []string{pubKey},
				Vars: map[string]envVarResolver{
					"API_KEY": {Kind: "literal", Value: "secret123"},
				},
			},
		},
	}
	manifestData, err := json.MarshalIndent(manifest, "", "  ")
	if err != nil {
		t.Fatal(err)
	}
	if err := os.WriteFile(filepath.Join(genDir, "env-manifest.json"), manifestData, 0644); err != nil {
		t.Fatal(err)
	}

	ctx := context.Background()
	req := BuildRequest{ProjectRoot: projectRoot, Force: false}

	// First build: must encrypt.
	out1, err := (envModule{}).Build(ctx, req)
	if err != nil {
		t.Fatalf("first build: %v", err)
	}
	if len(out1.Artifacts) == 0 {
		t.Fatal("expected artifacts on first build")
	}

	// Write artifacts to disk (simulate Builder.writeArtifact).
	for _, a := range out1.Artifacts {
		if err := os.MkdirAll(filepath.Dir(a.Path), 0755); err != nil {
			t.Fatal(err)
		}
		if err := os.WriteFile(a.Path, a.Content, a.Mode); err != nil {
			t.Fatal(err)
		}
	}

	// Second build: same plaintext and recipients → must produce identical bytes.
	// writeArtifact would then skip both .sops.json and .ts files.
	out2, err := (envModule{}).Build(ctx, req)
	if err != nil {
		t.Fatalf("second build: %v", err)
	}

	for _, a := range out2.Artifacts {
		isSops := strings.HasSuffix(a.Path, ".sops.json")
		isTS := strings.HasSuffix(a.Path, ".ts")
		if !isSops && !isTS {
			continue
		}
		existing, err := os.ReadFile(a.Path)
		if err != nil {
			t.Fatalf("read artifact %s: %v", a.Path, err)
		}
		if !bytes.Equal(a.Content, existing) {
			t.Errorf("artifact %s changed on second build (re-encryption occurred)", a.Path)
		}
	}
}
