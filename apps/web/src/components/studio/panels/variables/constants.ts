/**
 * Virtual selector retained for plaintext variables.
 */
export const SHARED_GROUP = "shared";

/**
 * Map mode selection to the variable ID prefix.
 *
 * Returns:
 *   - idPrefix: the path prefix for the variable ID (e.g., "/var/", "/secret/")
 *   - sopsGroup: legacy field retained for compatibility
 */
export function resolveGroup(
  group: string,
  mode: "plaintext" | "secret",
): { idPrefix: string; sopsGroup: string | null } {
  if (mode === "secret") {
    // Use the group name directly as the ID prefix — /dev/, /prod/, /staging/, etc.
    const sopsGroup = group || "shared";
    return { idPrefix: `/${sopsGroup}/`, sopsGroup };
  }
  return { idPrefix: "/var/", sopsGroup: null };
}
